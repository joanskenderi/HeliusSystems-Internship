// PRIMITIVE DATA TYPES

// Number
var number = 20;
console.log(number);
console.log(typeof number);

// String
var string = "Name";
console.log(string);
console.log(typeof string);

// Boolean (only TWO values: True or False)
var bool = true;
console.log(bool);
console.log(typeof bool);

// Undefined
var undefined;
console.log(undefined);
console.log(typeof undefined);

